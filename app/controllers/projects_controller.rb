class ProjectsController < ApplicationController
  before_action :set_project, only: [:show, :edit, :update, :destroy]

  # GET /projects
  # GET /projects.json
  def index
    @projects = Project.all
  end

  # GET /projects/1
  # GET /projects/1.json
  def show
  end

  # GET /projects/new
  def new
    @project = Project.new
  end

  # GET /projects/1/edit
  def edit
  end

  # POST /projects
  # POST /projects.json
  def create
    @project = Project.new(project_params)

    respond_to do |format|
      if @project.save
        format.html { redirect_to @project, notice: 'Project was successfully created.' }
        format.json { render :show, status: :created, location: @project }
      else
        format.html { render :new }
        format.json { render json: @project.errors, status: :unprocessable_entity }
      end
    end
  end

  # PATCH/PUT /projects/1
  # PATCH/PUT /projects/1.json
  def update
    respond_to do |format|
      if @project.update(project_params)
        format.html { redirect_to @project, notice: 'Project was successfully updated.' }
        format.json { render :show, status: :ok, location: @project }
      else
        format.html { render :edit }
        format.json { render json: @project.errors, status: :unprocessable_entity }
      end
    end
  end

  # DELETE /projects/1
  # DELETE /projects/1.json
  def destroy
    @project.destroy
    respond_to do |format|
      format.html { redirect_to projects_url, notice: 'Project was successfully destroyed.' }
      format.json { head :no_content }
    end
  end

  private
    # Use callbacks to share common setup or constraints between actions.
    def set_project
      @project = Project.find(params[:id])
    end

    # Never trust parameters from the scary internet, only allow the white list through.
    def project_params
      params.require(:project).permit(:name, :geometry_input_type, :climate_zone, :latitude, :longitude, :elevation, :street_address, :city, :state, :zip_code, :owner_name, :owner_title, :owner_organization, :owner_email, :owner_phone, :architect_name, :architect_title, :architect_organization, :architect_email, :architect_phone, :hvac_engineer_name, :hvac_engineer_title, :hvac_engineer_organization, :hvac_engineer_email, :hvac_engineer_phone, :weather_station, :hvac_auto_sizing, :exceptional_condition_complete_building, :exceptional_condition_exterior_lighting, :exceptional_condition_no_cooling_system, :exceptional_condition_rated_capacity, :exceptional_condition_water_heater, :exceptional_condition_narrative, :run_title, :compliance_type, :compliance_report_pdf, :compliance_report_xml)
    end
end
